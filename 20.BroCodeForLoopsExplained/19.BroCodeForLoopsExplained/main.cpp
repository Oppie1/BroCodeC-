#include <iostream>
using namespace std;


int main()

{
	for (int index = 1; index <= 3; index++) {
		cout << "Happy New Year" << '\n';
	}

	cout << '\n';

	for (int i = 1; i <= 3; i++) {
		cout << "Happy New Year" << '\n';
	}

	cout << '\n';

	for (int i = 1; i <= 5; i++) {
		cout << "Happy New Year" << '\n';
	}

	cout << '\n';

	for (int i = 1; i <= 10; i++) {
		cout << i << '\n';
	}
	cout << "Happy New Year" << '\n';

	cout << '\n';

	for (int i = 1; i <= 10; i += 2) {
		cout << i << '\n';
	}
	cout << "Happy New Year" << '\n';

	cout << '\n';

	for (int i = 0; i <= 10; i += 2) {
		cout << i << '\n';
	}
	cout << "Happy New Year" << '\n';

	cout << '\n';

	for (int i = 0; i <= 10; i += 3) {
		cout << i << '\n';
	}
	cout << "Happy New Year" << '\n';

	cout << '\n';

	for (int i = 10; i >= 0; i--) {
		cout << i << '\n';
	}

	cout << "Happy New Year" << '\n';

	cout << '\n';

	for (int i = 10; i >= 0; i -= 2) {
		cout << i << '\n';
	}
	cout << "Happy New Year" << '\n';

}